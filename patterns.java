public class patterns {
    public static void main(String[] args) {
        for(int i=0;i<4;i++){
            for(int j=0;j<4;j++){ //Do this 4 times for 4 rows.
                System.out.print("* "); //Print 4 stars.
            }
            System.out.println(" ");  //Go to the next line.
        }
    }
}
