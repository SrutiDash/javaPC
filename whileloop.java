public class whileloop {
    public static void main(String[] args) {
        int i=1;
        while(i<=5){
            System.out.println(i);
            i++;
        }

        int j=1;
        int sum=0;
        while(j<=5){
            sum+=j;
            j++;
        }
        System.out.println("sum os 5 natutal numbers are "+sum);
    }
}
